/**
 * Optimized Scroll Reveal for Markdown Elements
 * Automatically targets headings and list items generated by Jekyll
 */
function initMarkdownScrollReveal() {
    // Target your headers, horizontal rules, and list items dynamically
    const revealTargets = document.querySelectorAll('main h2, main h3, main hr, main li');
    
    const observerOptions = {
        root: null,
        rootMargin: '0px 0px -50px 0px', // Triggers slightly before entering focus
        threshold: 0.02
    };

    const revealObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
                revealObserver.unobserve(entry.target);
            }
        });
    }, observerOptions);

    revealTargets.forEach(target => {
        target.style.opacity = '0';
        target.style.transform = 'translateY(10px)';
        target.style.transition = 'opacity 0.4s ease-out, transform 0.4s ease-out';
        revealObserver.observe(target);
    });
}